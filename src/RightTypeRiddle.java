public class RightTypeRiddle {
    public static void main(String[] args){
        int a =1;
        double b=10.67;
        char c='M';
        boolean d=true;

        System.out.println(a+" is an Integer");
        System.out.println(b+" is Double");
        System.out.println(c+" is Character");
        System.out.println(d+" is Boolean");
    }
}
